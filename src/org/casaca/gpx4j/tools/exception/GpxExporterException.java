package org.casaca.gpx4j.tools.exception;

public class GpxExporterException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8364331701893200132L;

	public GpxExporterException() {
		super();
	}

	public GpxExporterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GpxExporterException(String arg0) {
		super(arg0);
	}

	public GpxExporterException(Throwable arg0) {
		super(arg0);
	}
}
