package org.casaca.gpx4j.tools.exception;

public class GpxChronometerException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 5065826858563883063L;

	public GpxChronometerException() {
		super();
	}

	public GpxChronometerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GpxChronometerException(String arg0) {
		super(arg0);
	}

	public GpxChronometerException(Throwable arg0) {
		super(arg0);
	}
}
