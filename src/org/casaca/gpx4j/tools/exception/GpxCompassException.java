package org.casaca.gpx4j.tools.exception;

public class GpxCompassException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -5008747060603866413L;

	public GpxCompassException() {
		super();
	}

	public GpxCompassException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public GpxCompassException(String arg0) {
		super(arg0);
	}

	public GpxCompassException(Throwable arg0) {
		super(arg0);
	}

}
